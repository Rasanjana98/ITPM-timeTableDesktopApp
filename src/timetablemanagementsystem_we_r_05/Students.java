    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        //Input field validation
        String input1 = yrSemTf.getText();
        String input2= prgrameEt.getSelectedItem().toString();
        String input3=(String)gNoS.getValue().toString();
        String input4=(String)subGNoS.getValue().toString(); 
        String input5 = GidTf.getText();
        String input6 = SubGIdTf.getText();
        
        
        try{
                if (input1.equals("") && input2.equals("Select programme") && input3.equals("0") && input4.equals("0") && input5.equals("") && input6.equals("")){
                    
                 AyrSem.setText("This field cannot be empty");
                 programeJl.setText("Select valied programme"); 
                 GNoJl.setText("This field cannot be 0"); 
                 SuGNOJl.setText("This field cannot be 0");
                 GidJl.setText("This field cannot be empty");
                 suGJl.setText("This field cannot be empty");
                  
                 JOptionPane.showMessageDialog(this, "Input fields cannot be empty","Error",JOptionPane.ERROR_MESSAGE);
                    
                }else if(input1.equals("")){
                   
                    AyrSem.setText("This field cannot be empty");
                    JOptionPane.showMessageDialog(this, "Academic year and semester field cannot be empty","Error",JOptionPane.ERROR_MESSAGE);
                    
                    }else if(input2.equals("Select programme")){
                       
                        programeJl.setText("Select valied programme");
                        JOptionPane.showMessageDialog(this, "Select valied programme","Error",JOptionPane.ERROR_MESSAGE);
                        
                    }else if(input3.equals("0")){
                        
                        GNoJl.setText("This field cannot be 0");
                        JOptionPane.showMessageDialog(this, "Select valied Group number","Error",JOptionPane.ERROR_MESSAGE);
                        
                    } else if(input4.equals("0")){
                        
                        SuGNOJl.setText("This field cannot be 0");
                        JOptionPane.showMessageDialog(this, "Select valied Sub group number","Error",JOptionPane.ERROR_MESSAGE);
                        
                    } else if(input5.equals("")){
                        
                        GidJl.setText("This field cannot be empty");
                        JOptionPane.showMessageDialog(this, "Group id field cannot be empty","Error",JOptionPane.ERROR_MESSAGE);
                        
                    } else if(input6.equals("")){
                        
                        suGJl.setText("This field cannot be empty");
                        JOptionPane.showMessageDialog(this, "Sub group id field cannot be empty","Error",JOptionPane.ERROR_MESSAGE);
                        
                    }
                
                
                else{
                        
             con= Database.getConnection();
             ps1 = con.prepareStatement("INSERT INTO student_group (StuG_id,Academic_yr_sem,Programme,Group_no,SubG_no,Group_id,SubG_id) values (0,?,?,?,?,?,?)");
           
           // ps.setInt(1, StuG_id);
            ps1.setString(1,input1);
            ps1.setString(2,input2);
            ps1.setString(3,input3);
            ps1.setString(4, input4);
            ps1.setString(5, input5);
            ps1.setString(6, input6);
            
           
            ps1.execute();
             JOptionPane.showMessageDialog(null, "Data Save Successfully");
            table_update();
            
                }
        }catch(Exception e){
        
        JOptionPane.showMessageDialog(this,e);
        System.out.println(e);
        }
        
              
    }//GEN-LAST:event_jButton3ActionPerformed
