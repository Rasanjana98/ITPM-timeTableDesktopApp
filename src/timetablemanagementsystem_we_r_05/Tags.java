    private void AddTagDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddTagDetailsActionPerformed
        // TODO add your handling code here:
        
        //Input field validation
        String input1 = TagName.getText();
        String input2 = TagCode.getText();
        String input3 = RelatedTag.getText();
        
        
        try{
            
        if(input1.equals("") && input2.equals("") && input3.equals("")){  //Input field validation
            
            TagJl.setText("This field cannot be empty");
            TagCodeJl.setText("This field cannot be empty");
            RelatedTJl.setText("This field cannot be empty");
           
        JOptionPane.showMessageDialog(this, "Input fields cannot be empty","Error",JOptionPane.ERROR_MESSAGE);
        }else if(input1.equals("")){
                  TagJl.setText("This field cannot be empty");
                  JOptionPane.showMessageDialog(this, "Tag name cannot be empty","Error",JOptionPane.ERROR_MESSAGE);
            }else if(input2.equals("")){
                   TagCodeJl.setText("This field cannot be empty");
                   JOptionPane.showMessageDialog(this, "Tag code cannot be empty","Error",JOptionPane.ERROR_MESSAGE);
            } else if(input3.equals("")){
                   RelatedTJl.setText("This field cannot be empty");
                   JOptionPane.showMessageDialog(this, "Related tag cannot be empty","Error",JOptionPane.ERROR_MESSAGE);
            }
        else{
              
  
             Database.setData("Insert into tag values('0','"+TagName.getText()+"','"+TagCode.getText()+"','"+RelatedTag.getText()+"')");
             table_update();
      
        //success msg
        JOptionPane.showMessageDialog(this, " added succesfully","Successful",JOptionPane.INFORMATION_MESSAGE);
       
        }
        }catch(Exception e){
        
        JOptionPane.showMessageDialog(this, "Invalid Input.Re enter details.","Error",JOptionPane.ERROR_MESSAGE);
        System.out.println(e);
        }
    }//GEN-LAST:event_AddTagDetailsActionPerformed

