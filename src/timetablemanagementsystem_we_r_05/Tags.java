    private void updateTagDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateTagDetailsActionPerformed
        

       
             DefaultTableModel d1 =(DefaultTableModel)jTable1.getModel();
            int selectIndex =jTable1.getSelectedRow();
            int tagID = Integer.parseInt(d1.getValueAt(selectIndex, 0).toString());
            
            
            String TagName = tagNameTF.getText();
            String TagCode = tagCodeTf.getText();
            String RelatedTag = relatedTagTf.getText();
            
            
            
            if(TagName.equals("")  ){
            JOptionPane.showMessageDialog(null,"Tag name is mondotory");
            } else if(TagCode.equals("")){
            JOptionPane.showMessageDialog(null,"Tag code is mondotory");
            }else if(RelatedTag.equals("")){
            JOptionPane.showMessageDialog(null,"Related is mondotory");
            }else{
                
            
            try {
                
            Connection conn =Database.getConnection();
            
            //PreparedStatement editStmt;
            pst = conn.prepareStatement("update tag set  Tag_name = ?,Tag_code=?, Related_tag=? where Tag_id= ?");
 
            pst.setString(1,TagName);
            pst.setString(2,(TagCode));
            pst.setString(3,RelatedTag);
            pst.setInt(4, tagID);
            
            pst.executeUpdate();
            pst.execute();
            JOptionPane.showMessageDialog(this, "Record Updated");
            table_update();
            
            tagNameTF.setText("");
            tagCodeTf.setText("");
            relatedTagTf.setText("");
            tagNameTF.requestFocus();
            
            
            
            } catch (Exception ex) {
         
                JOptionPane.showMessageDialog(this,ex);
                 System.out.println(ex);
            }
            

            }
            
                   
    }//GEN-LAST:event_updateTagDetailsActionPerformed
