    private void LoadTagDetailsToTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoadTagDetailsToTableActionPerformed
        // TODO add your handling code here:
        
        
        try{
        
            ResultSet rs= Database.getData("Select * from tag");
            DefaultTableModel tm = (DefaultTableModel)DisplayTag.getModel();
            tm.setRowCount(0);
            
            
             Object[] column = new Object[4];
            while(rs.next()){
                
             
             column[0] = rs.getInt("Tag_id");
             column[1] =  rs.getString("Tag_name");
             column[2] =  rs.getString("Tag_code");
             column[3] =  rs.getString("Related_tag");
           
             tm.addRow(column);
            }
        
    }catch(Exception e){
            JOptionPane.showMessageDialog(this,e);
            System.out.println(e);
            }
        SearchTag1.setText("");
    }//GEN-LAST:event_LoadTagDetailsToTableActionPerformed
